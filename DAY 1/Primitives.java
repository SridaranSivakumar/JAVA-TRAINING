package Day1Task;

public class Primitives{
	static int intval;
	static float floatval;
	static byte byteval;
	static short shortval;
	static double doubleval;
	static char charval;
	static boolean booleanval;
	static long longval;
public static void main(String[] args) {
   
   System.out.println("Byte: "+intval);
   System.out.println("Short: "+floatval);
   System.out.println("Int: "+byteval);
   System.out.println("Long: "+shortval);
   System.out.println("Float: "+doubleval);
   System.out.println("Double: "+charval);
   System.out.println("Char: "+booleanval);
   System.out.println("Boolean: "+longval);

}

}


